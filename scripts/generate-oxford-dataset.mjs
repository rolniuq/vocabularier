/**
 * Oxford 3000 Dataset Generator
 *
 * Reads The_Oxford_3000.txt, translates words to Vietnamese via Groq (free),
 * and outputs a TypeScript data file at src/data/extended-oxford.ts.
 *
 * Usage:
 *   GROQ_API_KEY=gsk_xxx node scripts/generate-oxford-dataset.mjs
 *
 * If no API key is set, it generates a file with empty Vietnamese translations
 * (you can fill them in later).
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// ── Configuration ──────────────────────────────────────────────────────────────

const WORDS_FILE = path.resolve(__dirname, '..', 'The_Oxford_3000.txt');
const OUTPUT_FILE = path.resolve(__dirname, '..', 'src', 'data', 'extended-oxford.ts');
const GROQ_API_KEY = process.env.GROQ_API_KEY;
const BATCH_SIZE = 50; // words per API call (max ~80 to stay within token limits)
const MODEL = 'llama-3.3-70b-versatile';

// ── Read & clean word list ─────────────────────────────────────────────────────

function readWords() {
  const text = fs.readFileSync(WORDS_FILE, 'utf-8');
  const seen = new Set();
  const words = [];

  for (const line of text.split('\n')) {
    const w = line.trim().replace(/\r$/, '');
    if (!w || w.startsWith('===') || w.length <= 2) continue;
    if (!/^[a-zA-Z]/.test(w)) continue;
    const lower = w.toLowerCase();
    if (seen.has(lower)) continue;
    seen.add(lower);
    words.push(w);
  }

  console.log(`📖 Read ${words.length} unique words from ${WORDS_FILE}`);
  return words;
}

// ── Translate a batch via Groq ─────────────────────────────────────────────────

async function translateBatch(words, batchNum, totalBatches) {
  const prompt = `You are a Vietnamese-English translator. Translate each English word to Vietnamese naturally and concisely.

For words with multiple meanings, pick the most common one.
Return ONLY a JSON object where keys are the English words and values are the Vietnamese translations.
No explanations, no extra text.

Words: ${words.join(', ')}`;

  const response = await fetch('https://api.groq.com/openai/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${GROQ_API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      model: MODEL,
      messages: [{ role: 'user', content: prompt }],
      temperature: 0.1,
      response_format: { type: 'json_object' },
    }),
  });

  if (!response.ok) {
    const err = await response.text();
    throw new Error(`Groq API error (batch ${batchNum}): ${response.status} ${err}`);
  }

  const data = await response.json();
  const content = data.choices?.[0]?.message?.content;
  if (!content) throw new Error(`Empty response from Groq (batch ${batchNum})`);

  const result = JSON.parse(content);
  const count = Object.keys(result).length;
  console.log(`  ✅ Batch ${batchNum}/${totalBatches} — ${count} words translated`);
  return result;
}

// ── Generate TypeScript file ───────────────────────────────────────────────────

function generateFile(allTranslations) {
  const entries = Object.entries(allTranslations).map(([word, vietnamese]) => {
    const escaped = vietnamese.replace(/"/g, '\\"');
    return `  { word: "${word}", definition: "", vietnamese: "${escaped}", synonyms: [] },`;
  });

  const content = `// Auto-generated by scripts/generate-oxford-dataset.mjs
// Total words: ${entries.length}

export interface OxfordWord {
  word: string;
  definition: string;
  vietnamese?: string;
  synonyms: string[];
}

export const extendedOxford3000: OxfordWord[] = [
${entries.join('\n')}
];
`;

  fs.writeFileSync(OUTPUT_FILE, content, 'utf-8');
  console.log(`\n📝 Wrote ${entries.length} words to ${OUTPUT_FILE}`);
}

// ── Main ───────────────────────────────────────────────────────────────────────

async function main() {
  console.log('🚀 Oxford 3000 Dataset Generator\n');

  const words = readWords();
  console.log(`🤖 Using model: ${MODEL}\n`);

  if (!GROQ_API_KEY) {
    console.warn('⚠️  No GROQ_API_KEY set. Generating file with empty Vietnamese translations.\n');
    const empty = Object.fromEntries(words.map(w => [w, '']));
    generateFile(empty);
    console.log('🔧 Set GROQ_API_KEY and re-run to auto-translate all words.');
    return;
  }

  const batches = [];
  for (let i = 0; i < words.length; i += BATCH_SIZE) {
    batches.push(words.slice(i, i + BATCH_SIZE));
  }

  console.log(`📦 Splitting into ${batches.length} batches of ${BATCH_SIZE}\n`);

  const allTranslations = {};
  let completed = 0;

  // Process batches sequentially to respect rate limits
  for (let i = 0; i < batches.length; i++) {
    const result = await translateBatch(batches[i], i + 1, batches.length);
    Object.assign(allTranslations, result);
    completed += Object.keys(result).length;

    // Small delay between batches to avoid rate limiting
    if (i < batches.length - 1) {
      await new Promise(r => setTimeout(r, 500));
    }
  }

  console.log(`\n🎯 Total translated: ${completed} words`);
  generateFile(allTranslations);
  console.log('✅ Done!');
}

main().catch(err => {
  console.error('\n❌ Error:', err.message);
  process.exit(1);
});
